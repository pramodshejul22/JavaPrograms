package AB_46_Batch;

public class C5_Datatypes 
{

	public static void main(String[] args) 
	{
			byte a=10;
			System.out.println(a);
			short b=9872;
			System.out.println(b);
			int c=765434567;
			System.out.println(c);
			long d=765434567;
			System.out.println(d);
			float a1=2.34f;      //must end with f
			System.out.println(a1);
			double a2=765.432;
			System.out.println(a2);
			char a3='Z';   //char always use in single quotes
			System.out.println(a3);
			boolean a4=true;
			System.out.println(a4);
			boolean a5=false;
			System.out.println(a5);
			String name="Pramod Shejul"; //always use double quotes in string
			System.out.println(name);		

	}

}
